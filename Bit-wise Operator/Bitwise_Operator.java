public class Bitwise_Operator {
    public static void main (String args[] ){
        System.out.println( 5 & 6 );
        System.out.println( 5 | 6 );
        System.out.println( 5 ^ 6 );
        System.out.println( ~10 );
        System.out.println( 5 << 2 );
        System.out.println(  6 >> 2);
    }
}
